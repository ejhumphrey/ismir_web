<h2>Who are we?</h2>
<p>
  The International Society for Music Information Retrieval (ISMIR) is a non-profit
  organisation which, among other things, oversees the organisation of the ISMIR
  Conference. The ISMIR conference is held annually and is the world's leading
  research forum on processing, searching, organising and accessing music-related data.
</br>
</p>
<p>
    The Society reports recent activities at the yearly ISMIR Community Meeting (held during the Conference). The slide deck for the 2016 community meeting may be accessed at <a href="https://docs.google.com/presentation/d/1PGFeUkegm4T-XlKNQa-w37dOQvVj2AZQBZl0_ceQ1Tc/pub?start=false&loop=false&delayms=3000">this link</a>.
</p>

