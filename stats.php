<h2>ISMIR Paper and Membership Statistics</h2>
<p>The annual Conference of the <a href="http://www.ismir.net/">International Society for Music Information Retrieval</a> (ISMIR) is the world&#8217;s leading research forum on processing, searching, organizing and accessing music-related data. The ISMIR Conference provides a meeting place for the discussion of MIR-related research. Its main goal is to foster the exchange of ideas across disciplines by bringing together researchers, developers, educators, librarians, students, and professional users.</p>
<p>ISMIR is a non-profit organization which, among other things, oversees the organization of the ISMIR Conference.</p>
