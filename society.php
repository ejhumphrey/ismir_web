  <h2>About the Society</h2>
  <p>The International Society of Music Information Retrieval is a non-profit
  organization seeking to advance the access, organization, and understanding
  of music information. As a field, music information retrieval focuses on the
  research and development of computational systems to help humans
  better make sense of this data, drawing from a diverse set of disciplines,
  including, but my no means limited to, music theory, computer science, psychology,
  neuroscience, library science, electrical engineering, and machine learning.</p>

  <p>
  More formally, the objects of the Corporation are:
  <ol>
  <li>to foster the exchange of ideas between and among members whose activities,
  though diverse, stem from a common interest in music information retrieval, </li>
  <li>to stimulate research, development, and improvement in teaching in all
  branches of music information retrieval, </li>
  <li>to encourage publication and distribution of theoretical, empirical, and
  applied studies, </li>
  <li>to cooperate with representatives of other organizations and disciplines
  toward the furtherance of music information retrieval, and </li>
  <li>to support and encourage diversity in membership and the disciplines involved
  as a fundamental aspect of the society.</li>
  </ol>
  </p>

  <p>ISMIR was incorporated in Canada on July 4, 2008. It was previously run by a Steering Committee.</p>

